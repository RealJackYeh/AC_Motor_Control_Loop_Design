wn=50;
J=0.00016;
J1=0.00016*0.5;
g1=2*wn;
g2=2*wn^2;
g3=-J*wn^3;
kp=g2*J;
ki=-g3;
Gc_wo_Te=tf([g1*J J*g2 -g3],[J g1*J J*g2 -g3]);
Gc_butter=tf([wn^3],[1 2*wn 2*wn^2 wn^3]);
Gc_w_Te=tf([J1 g1*J J*g2 -g3],[J g1*J J*g2 -g3]);
h=bodeoptions;
h.PhaseMatching='on';
h.Title.FontSize = 14;
h.XLabel.FontSize = 14;
h.YLabel.FontSize = 14;
h.TickLabel.FontSize = 14;
bodeplot(Gc_wo_Te,'-b',Gc_w_Te,'-.b',Gc_butter,'-g',{1,100000},h);
legend('Luenberger-without-Te', 'Luenberger-with-Te', 'Butterworth');
h = findobj(gcf,'type','line');
set(h,'linewidth',2);
grid on;